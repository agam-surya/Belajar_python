a = 17
b = 3

# tambah
hasil = a + b
print(a, "+", b, "=", hasil)

# kurang
hasil = a - b
print(a, "-", b, "=", hasil)

# kali
hasil = a * b
print(a, "*", b, "=", hasil)

# bagi
hasil = a / b
print(a, "/", b, "=", hasil)

# eksponen/pangkat
hasil = a ** b
print(a, "**", b, "=", hasil)

# modulus/sisa bagi
hasil = a % b
print(a, "%", b, "=", hasil)

# floor division / kebalikan dari modulus (pembulatan dari pembagian float ke integer)
hasil = a // b
print(a, "//", b, "=", hasil)

